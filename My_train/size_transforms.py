    def __init__(self, inf_scales):
        self.inf_scales = inf_scales

    def multiscale(self, val_img):
        h, w = val_img.size
        for scale in self.inf_scales:
            img_scaled = val_img.resize((int(h * scale), int(w * scale)), Image.CUBIC)
